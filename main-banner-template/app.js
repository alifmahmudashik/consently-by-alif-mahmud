async function getCookieData() {
  if (control.showBanner) {
    const url = "https://consent-banner-template-1498a0.gitlab.io/cookie.json";

    try {
      const response = await fetch(url);
      const data = await response.json();

      // Store matched dataKeys to identify unmatched later
      let matchedCookies = [];

      data.forEach(function (cookie) {
        if (getWebCookies.includes(cookie.data_key)) {
          matchedCookies.push(cookie.data_key);

          Object.keys(cookieInfo).forEach(function (bannerCategory) {
            if (cookie.category.toLowerCase() == bannerCategory) {
              if (!cookieInfo[bannerCategory][cookie.data_controller]) {
                cookieInfo[bannerCategory][cookie.data_controller] = [];
              }
              cookieInfo[bannerCategory][cookie.data_controller].push(cookie);
            }
          });
        }
      });

      // Handle unmatched cookies
      getWebCookies.forEach(function (cookieName) {
        if (!matchedCookies.includes(cookieName)) {
          // Create unknown object without UUID
          let unknownCookie = {
            ID: "", // No random ID
            platform: "Website",
            category: "Unknown",
            data_key: cookieName,
            domain: window.location.host,
            description: "Cookie info is unknown",
            retention_period: "Unknown",
            data_controller: "Website",
            privacy_rights_portals: `${control.privacyPage}`,
            wildcard_match: 1,
          };

          if (!cookieInfo.unknown["Website"]) {
            cookieInfo.unknown["Website"] = [];
          }
          cookieInfo.unknown["Website"].push(unknownCookie);
        }
      });
    } catch (error) {
      console.error(error.message);
    }
  }
}

function onVisit() {
  var checkAdsUser = false;

  let params = new URLSearchParams(document.location.search);

  if (params.get("gclid") || params.get("fbclid")) {
    checkAdsUser = true;
  }

  let adsCookies = ["_gcl_aw", "_fbc"];

  let webCookies = document.cookie.split(";");

  webCookies.forEach(function (cookie) {
    let currentCookie = cookie.split("=")[0].trim();

    adsCookies.forEach(function (adsCookie) {
      if (adsCookie == currentCookie) {
        checkAdsUser = true;
      }
    });
  });

  if (checkAdsUser) {
    control.showBanner = false;
  }

  if (control.enableConsentOnSpecificPage) {
    disableConsentPageList.forEach(function (disablePath) {
      if (window.location.pathname.includes(disablePath)) {
        console.log("banner is disbale on this page");
        control.showBanner = false;
      }
    });
  }

  if (control.showBanner) {
    var checkChoice = JSON.parse(localStorage.getItem("bannerChoice"));

    if (checkChoice) {
      gtag("consent", "update", checkChoice);
    } else {
      gtag("consent", "default", consent);
    }

    dataLayerPush("consent_page_view");
  } else {
    Object.keys(consent).forEach(function (c) {
      consent[c] = "granted";
    });

    gtag("consent", "default", consent);

    dataLayerPush("consent_page_view");
  }
}

